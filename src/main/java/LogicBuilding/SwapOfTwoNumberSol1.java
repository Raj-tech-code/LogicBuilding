package main.java.LogicBuilding;

public class SwapOfTwoNumberSol1 {

    public static void main(String[] arg){

        int a = 10;
        int b = 20;

        int temp = a;
        a = b;
        b = temp;
        System.out.println("Value of a:"+a);
        System.out.println("Value of b:"+b);

    }
}
